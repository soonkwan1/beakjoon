#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;

int main(){
    cin.tie(NULL);
    ios_base::sync_with_stdio(false);

    int n;
    cin >> n;

    int dp[500001] = {0, };
    dp[1] = 1;
    dp[2] = 2;
    
    for (int i=3; i<=500000; i++){
        if (i % 2 == 0){
            dp[i] = dp[i / 2] * 2;
        }
        else{
            if (i/2 == dp[i/2]){
                dp[i] = 2;
            }
            else{
                dp[i] = 2 * dp[i/2] + 2;
            }
        }
    }

    cout << dp[n] << "\n";
    
    return 0;
}

